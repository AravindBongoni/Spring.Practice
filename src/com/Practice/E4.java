package com.Practice;

public class E4 {
	int id;
	String name;
	String salary;
	
	
	public E4(int id, String name, String salary) {
		
		this.id = id;
		this.name = name;
		this.salary = salary;
	}
	@Override
	public String toString() {
		return "E4 [id=" + id + ", name=" + name + ", salary=" + salary + "]";
	}
	

}
